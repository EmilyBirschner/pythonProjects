'''Tipos int e float
int -> numero inteiro
o tipo int representa qlqr número (+ ou -) sem sinal é +

float-> numero com ponto flutuante
o tipo float representa qlqr número (+ ou -) com ponto flutuante, sem sinal é +

A classe(função) TYPE mostra o tipo que o Python inferiu ao valor
'''

print(11) #int
print(-11) #int
print(0) #int

print(1.1, 10.11) #float
print(-1.1, 0.0) #float

print(type(1.9)) #vai mostrar o tipo do parâmetro que foi passado
print(type("Uma string qlqr")) #vai mostrar o tipo do parâmetro que foi passado
print(type("Uma string qlqr"), type(1), type(5.9)) #vai mostrar o tipo do parâmetro que foi passado


