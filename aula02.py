# Usando a função PRINT
# \r\n -> CRLF
# \n -> LF
print(12, 34) #argumentos não nomeados
print(12, 34, sep='-')# sep é um novo tipo de separador para argumentos não nomeados
print(12, 34, sep='-', end='\n')# end é no fim do print quebrar a linha mas, já faz automaticamente
print(12, 34, sep='-', end='\nquebra de linha')# end é no fim do print quebrar a linha mas, já faz automaticamente
